package com.alphatica.genotick.utility;

public class FastMath {
    public static int pow2(int v) {
        return v * v;
    }
    
    public static int pow3(int v) {
        return v * v * v;
    }
    
    public static int pow4(int v) {
        return v * v * v * v;
    }
    
    public static int pow5(int v) {
        return v * v * v * v * v;
    }
    
    public static int pow6(int v) {
        return v * v * v * v * v * v;
    }
    
    public static double pow2(double v) {
        return v * v;
    }
    
    public static double pow3(double v) {
        return v * v * v;
    }
    
    public static double pow4(double v) {
        return v * v * v * v;
    }
    
    public static double pow5(double v) {
        return v * v * v * v * v;
    }
    
    public static double pow6(double v) {
        return v * v * v * v * v * v;
    }
}
